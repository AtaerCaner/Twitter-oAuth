package com.sahibinden.ataercanercelik.data.exception.error412;

public class PINException extends Exception {

  public PINException() {
    super();
  }

  public PINException(final String message) {
    super(message);
  }

  public PINException(final String message, final Throwable cause) {
    super(message, cause);
  }

  public PINException(final Throwable cause) {
    super(cause);
  }
}
