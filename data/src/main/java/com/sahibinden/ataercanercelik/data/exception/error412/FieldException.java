package com.sahibinden.ataercanercelik.data.exception.error412;

public class FieldException extends Exception {

  public FieldException() {
    super();
  }

  public FieldException(final String message) {
    super(message);
  }

  public FieldException(final String message, final Throwable cause) {
    super(message, cause);
  }

  public FieldException(final Throwable cause) {
    super(cause);
  }
}
