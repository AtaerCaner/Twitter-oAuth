package com.sahibinden.ataercanercelik.data.exception.error412;

public class ApiKeyException extends Exception {

  public ApiKeyException() {
    super();
  }

  public ApiKeyException(final String message) {
    super(message);
  }

  public ApiKeyException(final String message, final Throwable cause) {
    super(message, cause);
  }

  public ApiKeyException(final Throwable cause) {
    super(cause);
  }
}
